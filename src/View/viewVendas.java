    /**
     * Metodo de somar o valor total de produto
     */
    private void somarValorTotalProduto(){
        double soma=0, valor;
        int cont = jTablePVendas.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) jTablePVendas.getValueAt(i, 4);
            soma = soma + valor;
            jTextFieldValorTotal.setText(String.valueOf(soma));
            aplicarDesconto();
        }
        
    }
