    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        // Adiciona Produtos
        if(jTextFieldQuantidade.getText().equals("")){
           JOptionPane.showMessageDialog(this, "Preeche todos os campos"," Atencao ", JOptionPane.WARNING_MESSAGE);
        }else{
         modelCProduto = controllerCProduto.getCProdutoController(Integer.parseInt(jTextFieldCodigo.getText()));
         DefaultTableModel model = (DefaultTableModel) jTablePVendas.getModel();
         int cont = 0;
         double quantidade = 0;
         quantidade = Double.parseDouble(jTextFieldQuantidade.getText());
         
            for (int i = 0; i < cont; i++) {
                model.setNumRows(0);
            }
                model.addRow(new Object[]{
                    modelCProduto.getId(),
                    modelCProduto.getNome(),
                    jTextFieldQuantidade.getText(),
                    modelCProduto.getValor(),
                    quantidade * modelCProduto.getValor()
                });
                somarValorTotalProduto();
        }
        
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

