    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        // Confirmar as vendas
        int codigoVenda = 0, codigoProduto=0;
        double desconto =0;
        listaModelVendasProdutos = new ArrayList<>();
        if(jTextFieldDesconto.getText().equals("")){
             desconto = 0;
        }else{
             desconto = Double.parseDouble(jTextFieldDesconto.getText());
        }
        
        modelVenda.setCliente(Integer.parseInt(jTextFieldCodigo.getText()));
        try {
            modelVenda.setVenDataVenda(blDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
        }
        modelVenda.setVendaValorLiquido(Double.parseDouble(jTextFieldValorTotal.getText()));
        modelVenda.setVendaValorProduto(Double.parseDouble(jTextFieldValorTotal.getText()) + desconto);
     
           modelVenda.setVenDesconto(0);
       
           modelVenda.setVenDesconto(desconto); 
        
        
        // Salva a venda
        codigoVenda = controllerVendas.salvarVendaController(modelVenda);
        if(codigoVenda>0){
          JOptionPane.showMessageDialog (this, " Dados gravados com sucesso "," Atencao ", JOptionPane.WARNING_MESSAGE);  
          
        }else{
           JOptionPane.showMessageDialog (this, " Erro ao gravar os dados "," Erro ", JOptionPane.ERROR_MESSAGE); 
        }
        int cont = jTablePVendas.getRowCount();
        for (int i = 0; i < cont; i++) {
            codigoProduto = (int) jTablePVendas.getValueAt(i, 0);
            modelVendasProdutos = new ModelVendasProdutos();
            modelCProduto = new ModelCProduto();
            modelVendasProdutos.setProduto((codigoProduto));
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVenProValor((double) jTablePVendas.getValueAt(i, 3));
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jTablePVendas.getValueAt(i, 2).toString()));
            //Produto
            modelCProduto.setId((codigoProduto));
            modelCProduto.setQuant(controllerCProduto.getCProdutoController(codigoProduto).getQuant()
                    - Integer.parseInt(jTablePVendas.getValueAt(i, 2).toString()));
            listaModelCProduto.add(modelCProduto);
            
            listaModelVendasProdutos.add(modelVendasProdutos);
        }
        //Salvar os produtos da venda
        if(controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)){
            //Alterar o stock de produtos
            controllerCProduto.alterarStockCProdutoController(listaModelCProduto);
           JOptionPane.showMessageDialog (this, " Dados gravados com sucesso "," Atencao ", JOptionPane.WARNING_MESSAGE); 
           carregarVendas();
           limparFormulario();
        }else{
           JOptionPane.showMessageDialog (this, " Erro ao gravar os dados "," Erro ", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

